// name:  fill_picture() 
// descrption:Fill with Picture
// param: fill_Data.using the software to get the data.
// return: none.
// version: VER1.0.0
// date: 17-June-2019
// attention: none
//========================================================================
void fill_picture(unsigned char fill_Data)
{
	unsigned char m,n;
	for(m=0;m<8;m++)
	{
		OLED_WR_Byte(0xb0+m,0);		//page0-page1
		OLED_WR_Byte(0x00,0);		//low column start address
		OLED_WR_Byte(0x10,0);		//high column start address
		for(n=0;n<128;n++)
			{
				OLED_WR_Byte(fill_Data,1);
			}
	}
}


