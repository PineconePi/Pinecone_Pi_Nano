//========================================================================  			    
void OLED_IIC_Setup(void)
{ 
	
  OLED_WR_Byte(0xAE,OLED_CMD);//--display off
	OLED_WR_Byte(0x00,OLED_CMD);//---set low column address
	OLED_WR_Byte(0x10,OLED_CMD);//---set high column address
	OLED_WR_Byte(0x40,OLED_CMD);//--set start line address  
	OLED_WR_Byte(0xB0,OLED_CMD);//--set page address
	OLED_WR_Byte(0x81,OLED_CMD); // contract control
	OLED_WR_Byte(0xFF,OLED_CMD);//--128   
	OLED_WR_Byte(0xA1,OLED_CMD);//set segment remap 
	OLED_WR_Byte(0xA6,OLED_CMD);//--normal / reverse
	OLED_WR_Byte(0xA8,OLED_CMD);//--set multiplex ratio(1 to 64)
	OLED_WR_Byte(0x3F,OLED_CMD);//--1/32 duty
	OLED_WR_Byte(0xC8,OLED_CMD);//Com scan direction
	OLED_WR_Byte(0xD3,OLED_CMD);//-set display offset
	OLED_WR_Byte(0x00,OLED_CMD);//
	
	OLED_WR_Byte(0xD5,OLED_CMD);//set osc division
	OLED_WR_Byte(0x80,OLED_CMD);//
	
	OLED_WR_Byte(0xD8,OLED_CMD);//set area color mode off
	OLED_WR_Byte(0x05,OLED_CMD);//
	
	OLED_WR_Byte(0xD9,OLED_CMD);//Set Pre-Charge Period
	OLED_WR_Byte(0xF1,OLED_CMD);//
	
	OLED_WR_Byte(0xDA,OLED_CMD);//set com pin configuartion
	OLED_WR_Byte(0x12,OLED_CMD);//
	
	OLED_WR_Byte(0xDB,OLED_CMD);//set Vcomh
	OLED_WR_Byte(0x30,OLED_CMD);//
	
	OLED_WR_Byte(0x8D,OLED_CMD);//set charge pump enable
	OLED_WR_Byte(0x14,OLED_CMD);//
	
	OLED_WR_Byte(0xAF,OLED_CMD);//--turn on oled panel
  OLED_Clear();
}  









static void delay()
{
	unsigned int i=50;
	while(i--);
}


// IIC
void OLED_IIC_Start()
{
	OLED_SCLK_SET() ;
	OLED_SDIN_SET();
	 delay();
	OLED_SDIN_CLR();
	 delay();
	OLED_SCLK_CLR();
	 delay();
}

void OLED_IIC_Stop()
{
	OLED_SCLK_SET() ;
	 delay();
	OLED_SDIN_CLR();
	delay();
	OLED_SDIN_SET();
	delay();
}


void OLED_IIC_Wait_Ack()
{
	OLED_SCLK_SET() ;
	delay();
	OLED_SCLK_CLR();
	delay();
}


void Write_OLED_IIC_Byte(unsigned char OLED_IIC_Byte)
{
	unsigned char i;
	unsigned char m,da;
	da=OLED_IIC_Byte;
	OLED_SCLK_CLR();
	for(i=0;i<8;i++)		
	{
			m=da;
		m=m&0x80;
		if(m==0x80)
		{OLED_SDIN_SET();}
		else OLED_SDIN_CLR();
			da=da<<1;
		OLED_SCLK_SET();
		OLED_SCLK_CLR();
		}
}

void Write_OLED_IIC_Command(unsigned char OLED_IIC_Command)
{
   OLED_IIC_Start();
   Write_OLED_IIC_Byte(0x78);            //Slave address,SA0=0
	OLED_IIC_Wait_Ack();	
   Write_OLED_IIC_Byte(0x00);			//write command
	OLED_IIC_Wait_Ack();	
   Write_OLED_IIC_Byte(OLED_IIC_Command); 
	OLED_IIC_Wait_Ack();	
   OLED_IIC_Stop();
}


void Write_OLED_IIC_Data(unsigned char OLED_IIC_Data)
{
   OLED_IIC_Start();
   Write_OLED_IIC_Byte(0x78);			//D/C#=0; R/W#=0
	OLED_IIC_Wait_Ack();	
   Write_OLED_IIC_Byte(0x40);			//write data
	OLED_IIC_Wait_Ack();	
   Write_OLED_IIC_Byte(OLED_IIC_Data);
	OLED_IIC_Wait_Ack();	
   OLED_IIC_Stop();
}

void OLED_WR_Byte(unsigned dat,unsigned cmd)
{
	if(cmd)
	{
		Write_OLED_IIC_Data(dat);
	}
	else {
   Write_OLED_IIC_Command(dat);
	}
}


